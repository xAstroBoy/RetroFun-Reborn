namespace Xabbo.Core;

/// <summary>
/// Represents a special relationship type in the friend list.
/// </summary>
public enum Relation
{
    /// <summary>
    /// No special relation.
    /// </summary>
    None,
    /// <summary>
    /// Displays a heart icon next to the friend's name.
    /// </summary>
    Heart,
    /// <summary>
    /// Displays a smiley face icon next to the friend's name.
    /// </summary>
    Smile,
    /// <summary>
    /// Displays a skull icon next to the friend's name.
    /// </summary>
    Skull
}
